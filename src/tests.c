static int test_equality(byte *expected, byte *actual, size_t size) {
    
    int test_failed = 0;
    
    printf("\n\nexpected---------------------------\n");
    for (int i = 0; i < size; i++) {
        if (expected[i] != actual[i]) {
            test_failed = 1;
        }
        if ((i + 1) % 4 == 0)
            printf("%02" PRIx32 " ", expected[i]);
        else
            printf("%02" PRIx32, expected[i]);
        
        if ((i + 1) % 16 == 0)
            printf("\n");
    }
    
    printf("actual-----------------------------\n");
    for (int i = 0; i < size; i++) {
        if ((i + 1) % 4 == 0)
            printf("%02" PRIx32 " ", actual[i]);
        else
            printf("%02" PRIx32, actual[i]);
        
        if ((i + 1) % 16 == 0)
            printf("\n");
    }
    
    if (test_failed == 1)
        printf("--> ❌ Test failed.\n\n");
    else
        printf("--> ✅ Test succeeded!\n\n");
    
    return test_failed;
}
